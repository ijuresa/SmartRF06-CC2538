/*******************************************************************************
* @brief    Initialize digital pin for LEDs on CC2538
********************************************************************************
* @return   Nothing
********************************************************************************
* @date     2017-07-04
*******************************************************************************/
void LED_init() {
    GPIOPinTypeGPIOOutput(LED_PORT_GPIO_BASE, LED_PDIO);
    GPIOPinWrite(LED_PORT_GPIO_BASE, LED_PDIO, SET_LOW);
}

/*******************************************************************************
* @brief    Digital Write for LED dPin
********************************************************************************
* @return   Nothing
********************************************************************************
* @date     2017-07-04
*******************************************************************************/
static inline void LED_digiWrite(uint8_t value) {
    if(value > SET_HIGH) {
        // Set error
        return;
    }
    GPIOPinWrite(LED_PORT_GPIO_BASE, LED_PDIO, LED_PDIO);
}

/*******************************************************************************
* @brief    Turn LED ON
********************************************************************************
* @return   Nothing
********************************************************************************
* @date     2017-07-04
*******************************************************************************/
void LED_turnOn() {
    LED_digiWrite(SET_HIGH);
}

/*******************************************************************************
* @brief    Turn LED OFF
********************************************************************************
* @return   Nothing
********************************************************************************
* @date     2017-07-04
*******************************************************************************/
void LED_turnOff() {
    LED_digiWrite(SET_LOW);
}


